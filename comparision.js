// Comparison Operators.

//equal to (==)

let a=5;
let b=5;

console.log("a==b",a==b); //true


//Not equal to (!=)

console.log("a!=b",a!=b);//false

//Equal to & type (===)

console.log("a===b",a===b);//true

//Not equal to & type (!==)

console.log("a!==b",a!==b);//false

//Greater than (>)

console.log("a>b",a>b);//false

//Less than (<)

console.log("a<b",a<b);//false

//Greater than or equal to (>=)

console.log("a>=b",a>=b);//true

//Less than or equal to (<=)

console.log("a<=b",a<=b);//true